import entities.EnergyType;

import java.util.Collections;
import java.util.List;

public final class OutProducer {

    private int id;
    private int maxDistributors;
    private double priceKW;
    private EnergyType energyType;
    private int energyPerDistributor;
    private List<Stat> monthlyStats;

    public void sortMonthlyStats() {
        for (Stat stat : monthlyStats) {
            Collections.sort(stat.getDistributorsIds());
        }
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getMaxDistributors() {
        return maxDistributors;
    }

    public void setMaxDistributors(int maxDistributors) {
        this.maxDistributors = maxDistributors;
    }

    public double getPriceKW() {
        return priceKW;
    }

    public void setPriceKW(double priceKW) {
        this.priceKW = priceKW;
    }

    public EnergyType getEnergyType() {
        return energyType;
    }

    public void setEnergyType(EnergyType energyType) {
        this.energyType = energyType;
    }

    public int getEnergyPerDistributor() {
        return energyPerDistributor;
    }

    public void setEnergyPerDistributor(int energyPerDistributor) {
        this.energyPerDistributor = energyPerDistributor;
    }

    public List<Stat> getMonthlyStats() {
        return monthlyStats;
    }

    public void setMonthlyStats(List<Stat> monthlyStats) {
        this.monthlyStats = monthlyStats;
    }
}
